def get_connection() -> sqlite3.Connection:
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db() -> None:
    """Создаёт таблицу и заполняет тестовыми товарами при первом запуске."""
    conn = get_connection()
    cur = conn.cursor()

    # Пересоздаём таблицу с колонкой quantity
    cur.execute("""
        CREATE TABLE IF NOT EXISTS products (
            id       INTEGER PRIMARY KEY AUTOINCREMENT,
            name     TEXT NOT NULL,
            category TEXT NOT NULL,
            price    REAL NOT NULL,
            quantity INTEGER NOT NULL DEFAULT 0
        )
    """)
    # Добавляем колонку quantity если её нет (миграция для существующей БД)
    try:
        cur.execute("ALTER TABLE products ADD COLUMN quantity INTEGER NOT NULL DEFAULT 0")
    except Exception:
        pass
    conn.commit()

    count = cur.execute("SELECT COUNT(*) FROM products").fetchone()[0]
    if count == 0:
        random.seed(42)
        rows = []
        for category, items in CATALOG.items():
            for name, base_price in items:
                # Небольшой разброс цены ±10%
                price = round(base_price * random.uniform(0.9, 1.1), 2)
                quantity = random.randint(0, 150)
                rows.append((name, category, price, quantity))
        cur.executemany(
            "INSERT INTO products (name, category, price, quantity) VALUES (?, ?, ?, ?)",
            rows
        )
        conn.commit()
        print(f"[DB] Таблица заполнена {len(rows)} товарами по {len(CATALOG)} категориям.")

    conn.close()
